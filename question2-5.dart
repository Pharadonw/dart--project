import 'dart:io';

void main() {
  int count = 0; // ใช้นับจำนวนตัวเลขที่พิมพ์

  // วนลูปตั้งแต่ 1 ถึง 100
  for (int i = 1; i <= 100; i++) {
    // ตรวจสอบว่าเลขที่กำลังพิมพ์เป็น 41 หรือไม่
    if (i == 41) {
      continue;  // ข้ามการพิมพ์เลข 41
    }

    // พิมพ์ตัวเลข
    stdout.write("$i ");

    count++; // เพิ่มจำนวนตัวเลขที่พิมพ์

    // ถ้าพิมพ์ครบ 10 ตัวแล้ว ให้เว้นบรรทัด
    if (count == 10) {
      stdout.writeln(); // เว้นบรรทัด
      count = 0; // รีเซ็ตตัวนับ
    }
  }
}
