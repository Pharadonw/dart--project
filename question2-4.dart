void main() {
  // วนลูปเพื่อสร้างตารางสูตรคูณแม่ 1 ถึง 9
  for (int i = 1; i <= 9; i++) {
    print("ตารางสูตรคูณแม่ $i:");

    // ลูปสำหรับคูณเลข 1 ถึง 12
    for (int j = 1; j <= 12; j++) {
      int result = i * j;
      print("$i x $j = $result");
    }

    print(""); // เว้นบรรทัดระหว่างแม่แต่ละตัว
  }
}
